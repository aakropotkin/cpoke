#include <assert.h>
#include "player.h"
#include "pokemon.h"


/* -------------------------------------------------------------------------- */

  bool
use_shield( pvp_player_t * player )
{
  assert( player != NULL );
  if ( 0 < player->shields )
    {
      player->shields--;
      return true;
    }
  return false;
}


